def drop_path(x, keep_prob = 1.0, inplace = False):
    mask_shape = (x.shape[0],) + (1,) * (x.ndim - 1) 
    # remember tuples have the * operator -> (1,) * 3 = (1,1,1)
    mask = x.new_empty(mask_shape).bernoulli_(keep_prob)
    mask.div_(keep_prob)
    if inplace:
        x.mul_(mask)
    else:
        x = x * mask
    return x



class DropPath(nn.Module):
    def __init__(self, p: float = 0.5, inplace: bool = False):
        super(DropPath, self).__init__()
        self.p = p
        self.inplace = inplace

    def forward(self, x):
        if self.training and self.p > 0:
            x = drop_path(x, self.p, self.inplace)
        return x

    def __repr__(self):
        return f"{self.__class__.__name__}(p={self.p})"


class KANBlock(nn.Module):
    def __init__(self, dim, dim2=None, padding=1, drop=0., drop_path=0., act_layer=nn.GELU, norm_layer=nn.LayerNorm, no_kan=False):
        super().__init__()
        if dim2 is None:
            dim2 = dim
        self.drop_path = DropPath(drop_path) if drop_path > 0. else nn.Identity()
        self.norm2 = norm_layer(dim)
        mlp_hidden_dim = int(dim)
        self.fac = int(dim/dim2)

        self.layer = KANLayer(in_features=dim, padding=padding, hidden_features=mlp_hidden_dim, out_features=dim2, act_layer=act_layer, drop=drop, no_kan=no_kan)

        self.apply(self._init_weights)

    def _init_weights(self, m):
        if isinstance(m, nn.Linear):
            nn.init.normal_(m.weight, 0, 0.2) 
            if isinstance(m, nn.Linear) and m.bias is not None:
                nn.init.constant_(m.bias, 0)
        elif isinstance(m, nn.LayerNorm):
            nn.init.constant_(m.bias, 0)
            nn.init.constant_(m.weight, 1.0)
        elif isinstance(m, nn.Conv2d):
            fan_out = m.kernel_size[0] * m.kernel_size[1] * m.out_channels
            fan_out //= m.groups
            m.weight.data.normal_(0, math.sqrt(2.0 / fan_out))
            if m.bias is not None:
                m.bias.data.zero_()

    def forward(self, x, H, W):
        if self.fac==1:
            x = x + self.drop_path(self.layer(self.norm2(x), H, W)) 
        else: # note fac should never be < 1
            x1 = self.drop_path(self.layer(self.norm2(x), H, W))
            x2 = F.max_pool2d(x, (1,2), (1,2))
            x = x1 + x2
        return x
